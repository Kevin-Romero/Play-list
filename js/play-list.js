    var search = document.getElementById('search');
    var btnSearch = document.getElementById('btn-search');
    var resultado = document.getElementById('search-body');
    var numbreSong = document.getElementById('number-song-container');    

    var numeroCanciones = canciones.length;
                
    numbreSong.innerHTML += '<p>'+numeroCanciones+'</p>';
        
    function filtrar(){
        //console.log(search.value);
            
        resultado.innerHTML = '';
            

            
        var texto = search.value.toLowerCase();
        for(var cancion of canciones){
            var nombre = cancion.nombre.toLowerCase();
            var autor = cancion.autor.toLowerCase();
            var categoria = cancion.categoria.toLowerCase();                
                
            if(nombre.indexOf(texto) !== -1 || autor.indexOf(texto) !== -1 || categoria.indexOf(texto) !== -1){
                resultado.innerHTML += '<a href="'+cancion.link+'" target="_blank" >'+cancion.nombre+' - '+cancion.autor+' /  '+cancion.categoria+'</a>';
            }
            else{
                resultado.innerHTML += ''; 
            }
                
        }
    }
        
    btnSearch.addEventListener('click',filtrar);
        
    search.addEventListener('keyup', filtrar);
        
    filtrar();
        
